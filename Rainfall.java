public class Rainfall {
	public static void main(String[] args) {
		double A = 12.0;
		double B = 14.0;
		double C = 8.0;
		double Average = (A+B+C)/3.0;

		System.out.println("Rainfall for April \t " + A);
		System.out.println("Rainfall for May \t " + B);
		System.out.println("Rainfall for June \t " + C);
		System.out.println("The average rainfall for the 3 months is " + Average);
	}

}